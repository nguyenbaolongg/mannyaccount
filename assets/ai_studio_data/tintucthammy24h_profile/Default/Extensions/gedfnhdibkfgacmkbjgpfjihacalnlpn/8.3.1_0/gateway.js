function updateTexts(lang) {
    const data = i18nData[lang];
    if (!data) return;

    document.querySelectorAll('[data-lang-key]').forEach(el => {
        const key = el.getAttribute('data-lang-key');
        if (data[key]) {
            el.innerHTML = data[key];
        }
    });

    const guideLink = document.querySelector('.link-item[href*="github.com"]');
    if (guideLink) {
        if (lang === 'vi') guideLink.href = "https://github.com/duckmartians/Auto-Whisk-Multi-Version/blob/main/README_vi.md";
        else guideLink.href = "https://github.com/duckmartians/Auto-Whisk-Multi-Version";
    }
}

document.addEventListener('DOMContentLoaded', () => {
    const langSelect = document.getElementById('languageSelector');

    chrome.storage.local.get(['language'], (result) => {
        const savedLang = result.language || 'vi';
        if (langSelect.querySelector(`option[value="${savedLang}"]`)) {
            langSelect.value = savedLang;
            updateTexts(savedLang);
        } else {
            langSelect.value = 'en';
            updateTexts('en');
        }
    });

    langSelect.addEventListener('change', (e) => {
        const newLang = e.target.value;
        updateTexts(newLang);
        chrome.storage.local.set({ language: newLang });
    });

    document.getElementById('btn-v760').addEventListener('click', () => {
        window.location.href = 'v7.6.0/sidepanel.html';
    });

    document.getElementById('btn-v751').addEventListener('click', () => {
        window.location.href = 'v7.5.1/sidepanel.html';
    });

    document.getElementById('btn-local').addEventListener('click', () => {
        window.open('https://github.com/duckmartians/G-Labs-Automation', '_blank');
    });
});